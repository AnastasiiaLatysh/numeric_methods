# Функція для обрахунку полінома мого варіанту: x^4 - 2x^3 - 9x^2 - 3x - 9
def get_polynomial(x):
    return x ** 4 - 2 * x ** 3 - 9 * x ** 2 - 3 * x - 9


# Функція для обрахунку похідної полінома мого варіанту: 4x^3 - 6x^2 - 19x - 3
def get_polynomial_derivative(x):
    return 4 * x ** 3 - 6 * x ** 2 - 18 * x - 3
